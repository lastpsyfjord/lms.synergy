def main():
    # Считываем введенную строку и разбиваем на список строк по пробелам
    numbers = input().split()
    
    # Создаем пустое множество для отслеживания встреченных чисел
    seen = set()
    
    # Создаем список для хранения результатов
    result = []
    
    # Проходим по каждому числу в списке
    for num_str in numbers:
        # Преобразуем строку в целое число (можно использовать int или float, 
        # но по условию числа целые, так как ввод через пробел и целые числа)
        num = int(num_str)
        
        # Проверяем, встречалось ли число ранее
        if num in seen:
            result.append("YES")
        else:
            result.append("NO")
            # Добавляем число в множество встреченных чисел
            seen.add(num)
    
    # Выводим результаты построчно
    for res in result:
        print(res)

if __name__ == "__main__":
    main()