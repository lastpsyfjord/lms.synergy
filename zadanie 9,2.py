import sys

def main():
    # Чтение всех строк из стандартного ввода
    data = sys.stdin.read().splitlines()
    
    # Инициализация списков для чисел
    list1 = []  # Первый список чисел
    list2 = []  # Второй список чисел
    found_empty = False  # Флаг обнаружения пустой строки-разделителя
    
    # Обработка каждой строки ввода
    for line in data:
        stripped = line.strip()  # Удаление пробелов в начале и конце строки
        
        # Обработка первой части ввода (до пустой строки)
        if not found_empty:
            if stripped == '':  # Если встретилась пустая строка
                found_empty = True  # Устанавливаем флаг разделителя
            else:
                # Добавляем число в первый список
                list1.append(int(stripped))
        # Обработка второй части ввода (после пустой строки)
        else:
            if stripped != '':  # Игнорируем пустые строки
                # Добавляем число во второй список
                list2.append(int(stripped))
    
    # Преобразование списков в множества
    set1 = set(list1)  # Уникальные числа первого списка
    set2 = set(list2)  # Уникальные числа второго списка
    
    # Нахождение пересечения множеств (общих чисел)
    common = set1 & set2  # Альтернатива: set1.intersection(set2)
    
    # Вывод количества общих чисел
    print(len(common))

if __name__ == "__main__":
    main()